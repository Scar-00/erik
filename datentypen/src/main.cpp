int main(int argc, char **argv) {
    // typen und ihre breite(meistens)
    // ganze zahlen
	char c = 'a';       // 1 byte / 8 bit breit
    short s = 16;       // 2 byte / 16 bit breit
    int   i = 0xFF;     // 4 byte / 32 bit breit
    long long l = 0xFF; // 8 byte / 64 bit breit

    //floating point typen
    float f = 10.0f;    // 4 byte / 32 bit breit (meistens)
    double d = 10.0;    // 8 byte / 64 bit breit (meistens)
    return 0;
}
